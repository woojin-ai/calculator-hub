export function getCalculatorBySlug(slug: string): CalculatorMeta | undefined {
  return calculators.find((c) => c.slug === slug);
}

export function getCalculatorsByCategory(
  category: CalculatorCategory
): CalculatorMeta[] {
  return calculators.filter((c) => c.category === category);
}

export function getRelatedCalculators(
  slug: string,
  limit = 3
): CalculatorMeta[] {
  const current = getCalculatorBySlug(slug);
  const others = calculators.filter(
    (c) => c.slug !== slug && c.status === "live"
  );
  if (!current) return others.slice(0, limit);

  // override: relatedSlugs가 있으면 그 순서대로(존재·live·self 아님만) 반환
  if (current.relatedSlugs && current.relatedSlugs.length > 0) {
    const ordered = current.relatedSlugs
      .map((s) => getCalculatorBySlug(s))
      .filter(
        (c): c is CalculatorMeta =>
          !!c && c.status === "live" && c.slug !== slug
      );
    return ordered.slice(0, limit);
  }

  const sameCategory = others.filter((c) => c.category === current.category);
  const rest = others.filter((c) => c.category !== current.category);
  return [...sameCategory, ...rest].slice(0, limit);
}
